    public static double getTrajectorySimilarity(double dist, boolean isWithTime) {
        //
        double similarity = 0.0;
        DecimalFormat df = new DecimalFormat("0.000000");  //
        if (isWithTime) {
            similarity = Math.pow(0.99, dist);
        } else {
            similarity = Math.pow(0.8, dist);
        }
        return new BigDecimal(similarity).setScale(6,BigDecimal.ROUND_UP).doubleValue();
    }

