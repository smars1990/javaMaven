    public static double TotalDist(List<Point> pointList1, List<Point> pointList2,double Wdi,double Wda,boolean isWithTime) {
        List<Point> points1 = pointList1;
        List<Point> points2 = pointList2;

        int len1 = points1.size();
        int len2 = points2.size();
        double[][] step = new double[len1 + 1][len2 + 1];
        int k;
        for (k = 0; k < len1 + 1; k++) {
            step[k][0] = k * 10;
        }
        for (k = 0; k < len2 + 1; k++) {
            step[0][k] = k * 10;
        }

        for (int i = 0; i < len1; i++) {
            Point p1 = points1.get(i);
            for (int j = 0; j < len2; j++) {
                Point p2 = points2.get(j);

                double dist=0;
                if(isWithTime)
                    dist= PointDist(p1, p2,Wdi,Wda);
                else
                    dist= DistanceDist(p1, p2);
                    //System.out.println("("+p1.getLongitude()+","+p1.getLatitude()+"):"+"("+p2.getLongitude()+","+p2.getLatitude()+")="+dist);
                double replace = step[i][j] + dist;
                double insert = step[i][j + 1] + dist;
                double delete = step[i + 1][j] + dist;
                double min = replace > insert ? insert : replace;
                min = delete > min ? min : delete;
                step[i + 1][j + 1] = min;
            }
        }
        return step[len1][len2];
    }

